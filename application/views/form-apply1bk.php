					<ul style="font-size: 14px;">
					<li>▪ student enrolling in nationally recognised training for the first time, for example if you are studying at TAFE or with a private training organisation, completing an apprenticeship or skill set, certificate or diploma course;</li>
					<li>▪ school student completing nationally recognised training; or student continuing with nationally recognised training. </li>
					<li>▪ You are a continuing student if you are a student who has already started your course in a previous year (and not yet completed it) and will continue studying after 1 January 2015.</li>
					</ul>
