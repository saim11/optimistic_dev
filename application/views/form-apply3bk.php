				  <h2>5. Victorian Student Number (VSN)</h2>
				  <p>The VSN is a student identification number that is assigned by the Department of Education and Early Childhood Development to all students in government and non-government schools, and students in Vocational Education and Training institutions. The number, which is unique to each student, is a key identifier on a student’s school records, and will remain with the student throughout his or her education, until reaching the age of 25. The VSN is nine digits long, randomly assigned, and tied to identifying information about the student (name, gender and date of birth). The Education and Training Reform Act 2006 requires Optimistic Futures to collect and disclose your personal information for a number of purposes including the allocation to you of a Victorian Student Number and updating your personal information on the Victorian Student Register.</p>

				  <h2>6.A Unique Student Identifier (USI)</h2>
				  <p>Is a reference number made up of numbers and letters that gives students access to their USI account. A USI will allow an individual's USI account to be linked to the National Vocational Education and Training (VET) Data Collection allowing an individual to see all of their training results from all providers including all completed training units and qualifications. The Student Identifiers Act 2014, Standards for NVR Registered Training Organisations (RTO) 2014 and Student Identifiers Regulation 2014 require that the training Organisation: Collect a USI from each student, verify a USI supplied by a student and ensure a student has a valid USI before conferring a qualification or statement of attainment on that student. As of 1 January 2015, students will need to give their USI to each new training Organisation they enroll with. This will normally happen at enrolment</p>

